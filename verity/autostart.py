#!/usr/bin/env python3
"""Auto-start VERITY as a SILENT background harness, tied to YOUR main agent.

The goal (DJ): no bulky UI, nothing to remember — VERITY just *runs in the background* and your
main agent (Claude Code / Cursor / Codex / a shell) inherits it, the way switching to a better
model "just works". This wires a tiny, non-blocking, idempotent startup hook into the agent of
your choice. On every session it: (1) quietly git-syncs the repo so you're always current, and
(2) starts the OpenAI-compatible proxy floor (`verity.server` on :11500) if it isn't already up —
so any agent you point at OPENAI_BASE_URL=http://127.0.0.1:11500/v1 transparently gets failover +
the discipline gates.

IMPORTANT (learned 2026-06-15): an ANTHROPIC-format agent (Claude Code / Desktop) talks directly to
api.anthropic.com and CANNOT route through the OpenAI-compatible :11500 proxy — so the proxy alone
never gates its reasoning (`verity proof` stays empty; negative-claim/rule-6 lapses slip through).
For those agents, `--claude-code` ALSO installs a second SessionStart hook that injects VERITY's
core gates as standing CONTEXT every session — that is what actually disciplines the agent. (The
proxy still serves as the sovereign-failover floor + gates anything explicitly run via `verity
solve/ask` or routed through :11500.)

Universal coverage (the gates reach EVERY agent class):
  • Claude Code/Desktop (Anthropic-format) — SessionStart hook injects the gates as context.
  • Codex (codex 5.5)                       — gates block written to ~/.codex/AGENTS.md.
  • Gemini CLI                              — gates block written to ~/.gemini/GEMINI.md.
  • Local / OSS / any OpenAI-API agent      — point it at OPENAI_BASE_URL=http://127.0.0.1:11500/v1
    and the proxy gates it NATIVELY (failover + discipline) — no injection needed.

Usage:
  python3 -m verity autostart --claude-code   # Claude Code SessionStart hook (proxy + gate-inject)
  python3 -m verity autostart --codex         # inject gates into ~/.codex/AGENTS.md
  python3 -m verity autostart --gemini         # inject gates into ~/.gemini/GEMINI.md
  python3 -m verity autostart --all            # claude-code + codex + gemini + the OSS/proxy note
  python3 -m verity autostart --shell          # add a line to your shell profile (~/.zshrc etc.)
  python3 -m verity autostart --print          # print the startup script + manual steps
"""
from __future__ import annotations

import json
import os
import pathlib
import stat

REPO = str(pathlib.Path(__file__).resolve().parent.parent)
SCRIPT = pathlib.Path(os.path.expanduser("~/.verity-harness/autostart.sh"))
INJECT = pathlib.Path(os.path.expanduser("~/.verity-harness/verity-context-inject.sh"))
GUARD = pathlib.Path(os.path.expanduser("~/.verity-harness/stop_guard.py"))

_SCRIPT_BODY = f"""#!/usr/bin/env bash
# VERITY silent background harness — idempotent, fast, NON-BLOCKING (never delays your agent).
# Auto-generated by `verity autostart`. Safe to run on every session start.
REPO="{REPO}"
(
  # 1. quiet self-sync (stay current; ignore if offline / not a git checkout)
  [ -d "$REPO/.git" ] && git -C "$REPO" pull -q --rebase --autostash >/dev/null 2>&1
  # 2. start the proxy floor only if it isn't already answering
  if ! curl -s -m 1 http://127.0.0.1:11500/health >/dev/null 2>&1; then
    ( cd "$REPO" && nohup python3 -m verity.server >/dev/null 2>&1 & )
  fi
) >/dev/null 2>&1 &
exit 0
"""

# Context-injection for ANTHROPIC-FORMAT agents (Claude Code/Desktop). They talk straight to
# api.anthropic.com and CANNOT route through the OpenAI-compatible :11500 proxy — so the proxy
# alone can't gate their reasoning (verity proof stays empty). This emits VERITY's core gates as
# SessionStart context so the agent applies them manually every session. (DJ, 2026-06-15.)
_INJECT_BODY = f"""#!/usr/bin/env bash
# VERITY gate-injection — auto-generated by `verity autostart`. Prints VERITY's core discipline so
# an Anthropic-format agent (Claude Code) that bypasses the :11500 proxy still runs the gates.
cat <<'GATES'
[VERITY HARNESS — discipline gates ACTIVE this session]
The :11500 failover floor is up, but YOUR reasoning is NOT auto-routed through it (Anthropic-format
agents talk direct to the API) — so apply these gates MANUALLY, every task. Not optional:
 • RULE 0 — PRE-FLIGHT: before executing a goal, search the CURRENT best approach (may supersede
   training). FIND the answer, don't RECALL it.
 • RULE 6 — SEARCH-BEFORE-CONCLUDING (BLOCKER): never assert a NEGATIVE ("there's no X", "not
   possible", "doesn't exist", "unsolvable", "only way is Y", "it's down / broken / can't be fixed /
   it's a global/environmental outage") until you've actually INVESTIGATED. For a broken tool/service
   that means, IN THIS ORDER: (1) READ its logs, (2) ATTEMPT its documented repair/restart/refresh,
   (3) SEARCH the exact error where fixes live (the platform's own API, GitHub, Reddit, X, YouTube,
   Google, StackOverflow). "It errored / returned empty / timed out" is a SYMPTOM, not a diagnosis —
   find the ROOT CAUSE before concluding. Someone (or a log line) almost always has the real answer.
   (Real lapse 2026-06-15: called a QA backend a "global model outage" from empty responses + 4 port
   probes; the shim log showed ONE expired OAuth token, fixable with one command. The assumption cost
   the real answer and produced a weak "can't fix it" result. Don't repeat it.)
 • VERIFY (Borg): adversarially confirm each action actually worked; >=2 backends; no "done" on a vibe.
 • REUSE-FIRST: check installed tools + existing OSS BEFORE building.
 • CALIBRATE: label conclusions VERIFIED vs GUESS.
 • AUTONOMY (BLOCKER): once the user says "proceed" / "do this" / "do all of this", execute EVERY
   stated goal CONSECUTIVELY and autonomously to completion — do NOT stop to re-ask for confirmation
   (that wastes their time). Chain the goals; only pause for genuinely destructive, ambiguous, or
   outward-facing actions (deletes, money, publishing). Apply ALL gates above to EACH goal.
 • Multi-step / uncertain / stuck:  python3 -m verity solve "<goal>"  (fires the gates + logs the ledger).
GATES
curl -s -m 1 http://127.0.0.1:11500/health >/dev/null 2>&1 || ( cd "{REPO}" && nohup python3 -m verity.server >/dev/null 2>&1 & )
exit 0
"""


def write_script() -> pathlib.Path:
    SCRIPT.parent.mkdir(parents=True, exist_ok=True)
    SCRIPT.write_text(_SCRIPT_BODY)
    SCRIPT.chmod(SCRIPT.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP)
    return SCRIPT


def write_inject_script() -> pathlib.Path:
    INJECT.parent.mkdir(parents=True, exist_ok=True)
    INJECT.write_text(_INJECT_BODY)
    INJECT.chmod(INJECT.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP)
    return INJECT


def write_guard_script() -> pathlib.Path:
    """Install the mechanical stop-guard (catches premature 'it's down/impossible' negatives and
    'only you can' deferrals as the agent tries to stop). Copied from the repo to the stable
    state dir so it survives the repo moving."""
    GUARD.parent.mkdir(parents=True, exist_ok=True)
    src = pathlib.Path(REPO) / "hooks" / "stop_guard.py"
    if src.exists():
        GUARD.write_text(src.read_text())
        GUARD.chmod(GUARD.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP)
    return GUARD


def wire_claude_code() -> str:
    """Add a SessionStart hook to ~/.claude/settings.json (idempotent, preserves everything)."""
    write_script()
    write_inject_script()
    write_guard_script()
    cfg = pathlib.Path(os.path.expanduser("~/.claude/settings.json"))
    data = {}
    if cfg.exists():
        try:
            data = json.loads(cfg.read_text())
        except ValueError:
            return f"[refused] {cfg} is not valid JSON — fix it first, then re-run."
    hooks = data.setdefault("hooks", {})
    changed = []
    # SessionStart → sync + start the proxy floor (silent).
    starts = hooks.setdefault("SessionStart", [])
    if str(SCRIPT) not in json.dumps(starts):
        starts.append({"hooks": [{"type": "command", "command": f"bash {SCRIPT}"}]})
        changed.append("SessionStart→start")
    # SessionStart → inject the discipline gates as CONTEXT (Claude Code bypasses the proxy, so the
    # gates can't auto-fire on its reasoning; this is what actually disciplines an Anthropic agent).
    if str(INJECT) not in json.dumps(starts):
        starts.append({"hooks": [{"type": "command", "command": f"bash {INJECT}", "timeout": 5}]})
        changed.append("SessionStart→inject-gates")
    # SessionEnd → STOP the proxy so it closes when you exit your agent (no lingering RAM).
    stop_cmd = f"python3 -m verity stop"
    ends = hooks.setdefault("SessionEnd", [])
    if "verity stop" not in json.dumps(ends):
        ends.append({"hooks": [{"type": "command", "command": f"cd {REPO} && {stop_cmd}"}]})
        changed.append("SessionEnd→stop")
    # Stop / SubagentStop → the MECHANICAL overconfidence guard: blocks ending a turn on an
    # unverified "it's down/impossible" negative or a premature "only you can" deferral, with no
    # opt-out (the gate text was advisory and got rationalized past; this fires on a code condition).
    guard_cmd = f"python3 {GUARD}"
    for ev in ("Stop", "SubagentStop"):
        arr = hooks.setdefault(ev, [])
        if "stop_guard.py" not in json.dumps(arr):
            arr.append({"matcher": "", "hooks": [{"type": "command", "command": guard_cmd, "timeout": 10}]})
            changed.append(f"{ev}→overconfidence-guard")
    if not changed:
        return "[already wired] Claude Code SessionStart→start + SessionEnd→stop."
    cfg.write_text(json.dumps(data, indent=2))
    return (f"[wired] Claude Code: {', '.join(changed)}.\nVERITY now starts silently on session "
            "start and STOPS on session end — it closes when you do, no lingering process. "
            "(Safety net: the proxy also self-shuts-down after idle.)")


def wire_shell() -> str:
    write_script()
    prof = next((pathlib.Path(os.path.expanduser(p)) for p in ("~/.zshrc", "~/.bashrc", "~/.profile")
                 if pathlib.Path(os.path.expanduser(p)).exists()),
                pathlib.Path(os.path.expanduser("~/.zshrc")))
    line = f"[ -f {SCRIPT} ] && bash {SCRIPT}   # VERITY background harness"
    existing = prof.read_text() if prof.exists() else ""
    if str(SCRIPT) in existing:
        return f"[already wired] {prof}"
    with open(prof, "a") as f:
        f.write(f"\n{line}\n")
    return f"[wired] {prof} — new shells start the VERITY harness silently."


# Gates as a MANAGED markdown block, for agents whose context = a .md file (Codex AGENTS.md,
# Gemini GEMINI.md). Same gates as the Claude-Code injector, idempotently inserted between markers.
_GATES_MD = """<!-- VERITY-GATES:start (managed by `verity autostart` — re-run to update) -->
## VERITY discipline gates — apply to EVERY task (not optional)
- **Rule 0 — Pre-flight:** before executing a goal, search the CURRENT best approach (may supersede
  training). FIND the answer, don't RECALL it.
- **Rule 6 — Search-before-concluding (BLOCKER):** never assert a NEGATIVE ("there's no X", "not
  possible", "doesn't exist", "unsolvable", "only way is Y", "it's down / broken / can't be fixed /
  environmental outage") until you've INVESTIGATED. For a broken tool/service, IN ORDER: (1) READ its
  logs, (2) ATTEMPT its documented repair/restart/refresh, (3) SEARCH the exact error where fixes live
  (platform API, GitHub, Reddit, X, YouTube, Google, StackOverflow). "Errored / empty / timed out" is a
  SYMPTOM, not a diagnosis — find the ROOT CAUSE first. (Real lapse: a QA backend was called a "global
  outage" from empty responses; the log showed one expired OAuth token, fixable in one command.)
- **Verify (Borg):** adversarially confirm each action actually worked; >=2 backends; no "done" on a vibe.
- **Reuse-first:** check installed tools + existing OSS BEFORE building.
- **Calibrate:** label conclusions VERIFIED vs GUESS.
- **Autonomy:** once told "proceed / do this / do all of this", execute EVERY stated goal
  consecutively & autonomously to completion — don't stop to re-ask (pause only for destructive,
  ambiguous, or outward-facing actions).
- Multi-step / uncertain / stuck: run `python3 -m verity solve "<goal>"` (fires gates + logs ledger).
<!-- VERITY-GATES:end -->"""

import re as _re

def _ensure_md_block(path: pathlib.Path) -> str:
    path.parent.mkdir(parents=True, exist_ok=True)
    text = path.read_text() if path.exists() else ""
    pat = _re.compile(r"<!-- VERITY-GATES:start.*?VERITY-GATES:end -->", _re.S)
    if pat.search(text):
        new = pat.sub(_GATES_MD, text)
        if new == text:
            return f"[already current] {path}"
        path.write_text(new); return f"[updated] {path}"
    sep = "" if text.endswith("\n") or not text else "\n\n"
    path.write_text(text + sep + "\n" + _GATES_MD + "\n"); return f"[wired] {path}"

def wire_codex() -> str:
    write_script()
    msg = _ensure_md_block(pathlib.Path(os.path.expanduser("~/.codex/AGENTS.md")))
    return (f"Codex: {msg}\n  NOTE: if a bootstrap regenerates AGENTS.md, add the gates block to that "
            "template too (or re-run this). Codex also gates natively if pointed at OPENAI_BASE_URL=:11500/v1.")

def wire_gemini() -> str:
    write_script()
    return "Gemini: " + _ensure_md_block(pathlib.Path(os.path.expanduser("~/.gemini/GEMINI.md")))


def main(target: str) -> None:
    if target == "--claude-code":
        print(wire_claude_code())
    elif target == "--codex":
        print(wire_codex())
    elif target == "--gemini":
        print(wire_gemini())
    elif target == "--shell":
        print(wire_shell())
    elif target == "--all":
        print(wire_claude_code()); print(wire_codex()); print(wire_gemini())
        print("\nLocal / open-source / any OpenAI-API agent: no injection needed — point it at the")
        print("proxy and it inherits failover + ALL gates transparently:")
        print("  export OPENAI_BASE_URL=http://127.0.0.1:11500/v1")
    else:
        write_script()
        print("VERITY background-harness startup script written to:\n  " + str(SCRIPT) + "\n")
        print("Wire the GATES into your agent (context-injection — works for any agent):")
        print("  • Claude Code:  python3 -m verity autostart --claude-code   (SessionStart hook)")
        print("  • Codex:        python3 -m verity autostart --codex          (~/.codex/AGENTS.md)")
        print("  • Gemini:       python3 -m verity autostart --gemini         (~/.gemini/GEMINI.md)")
        print("  • Everything:   python3 -m verity autostart --all")
        print("  • Any shell:    python3 -m verity autostart --shell")
        print("\nLocal / OSS / OpenAI-API agents ALSO inherit the gates by routing through the proxy:")
        print("  export OPENAI_BASE_URL=http://127.0.0.1:11500/v1")


if __name__ == "__main__":
    import sys
    main(sys.argv[1] if len(sys.argv) > 1 else "--print")
